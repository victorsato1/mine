function focusFunc() {
  let parent = this.parentNode;
  parent.classList.add("focus");
}

function blurFunc() {
  let parent = this.parentNode;
  if (this.value == "") {
    parent.classList.remove("focus");
  }
}
function openc() {
  let subMenu = document.getElementById("subMenu");
  subMenu.classList.toggle("open")
}

